package com.codoid;

public class SwitchExpressions {
	public static void main(String[] args) {
		int n = 2;
		switch (n) {
		case 1:
			System.out.println("jan");
			break;
		case 2:
			System.out.println("feb");
			break;
		case 3:
			System.out.println("mar");
			break;
		default:
			System.out.println("in valid");
			break;

		}
	}

}
